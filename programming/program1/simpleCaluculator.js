// Simple calculator program

let a = 20
let b = 4
let symbol = "%"

if (symbol == "+"){
    console.log(a+b)
}
else if (symbol == "-"){
    console.log(a-b)
}

else if(symbol == "*"){
    console.log(a*b)
}

else if (symbol == "/"){
    console.log(a/b)
}
else if (symbol == "%"){
    console.log(a%b)
}